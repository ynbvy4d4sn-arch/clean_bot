def _drawdown_from_equity(equity_curve: pd.Series) -> pd.Series:
    """Compute drawdown series from an equity curve."""

    curve = equity_curve.astype(float).dropna()
    if curve.empty:
        return curve
    running_peak = curve.cummax()
    return curve / running_peak - 1.0


def _format_weight_block(weights: pd.Series, min_weight: float = 0.005) -> str:
    """Render a compact text block for weight vectors."""

    filtered = weights.astype(float).copy()
    filtered = filtered[filtered.abs() >= min_weight].sort_values(ascending=False)
    if filtered.empty:
        return "  (no weights above threshold)"
    return "\n".join(f"  {ticker}: {weight:.2%}" for ticker, weight in filtered.items())


def _safe_record_value(record: pd.Series, key: str, default: object = "n/a") -> object:
    """Read a value from a record without raising on missing columns."""

    return record.get(key, default)


def _format_number(value: object, digits: int = 6) -> str:
    """Format a numeric report value or return n/a."""

    try:
        number = float(value)
    except (TypeError, ValueError):
        return "n/a"
    if pd.isna(number):
        return "n/a"
    return f"{number:.{digits}f}"


def _format_percent(value: object, digits: int = 2) -> str:
    """Format a percentage report value or return n/a."""

    try:
        number = float(value)
    except (TypeError, ValueError):
        return "n/a"
    if pd.isna(number):
        return "n/a"
    return f"{number:.{digits}%}"


def plot_equity_curves(result: dict[str, pd.DataFrame], output_path: str | Path) -> Path:
    """Plot strategy equity and benchmark equity curves."""

    if not MATPLOTLIB_AVAILABLE or plt is None:
        raise RuntimeError("matplotlib is not available in the current Python environment.")

    daily = result["daily"].copy()
    benchmark_equity = result["benchmark_equity"].copy()

    figure, axis = plt.subplots(figsize=(11, 6))
    strategy_curve = daily.set_index("date")["equity"]
    strategy_curve.plot(ax=axis, linewidth=2.2, label="strategy", color="#174c7e")

    for column in benchmark_equity.columns:
        series = benchmark_equity[column].dropna()
        if not series.empty:
            series.plot(ax=axis, linewidth=1.6, alpha=0.85, label=column)

    axis.set_title("Equity Curves")
    axis.set_xlabel("Date")
    axis.set_ylabel("Equity")
    axis.grid(alpha=0.25)
    axis.legend(loc="best")
    figure.tight_layout()

    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    figure.savefig(path, dpi=150)
    plt.close(figure)
    return path


