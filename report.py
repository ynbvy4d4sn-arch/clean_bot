def create_report_artifacts(
    result: dict[str, pd.DataFrame],
    output_dir: str | Path,
    latest_decision_filename: str = "latest_decision_report.txt",
) -> ReportArtifacts:
    """Generate the full report artifact set and return all output paths."""

    output_path = Path(output_dir)
    table_paths = save_tables(result=result, output_dir=output_path)
    equity_curves_png = _safe_plot(
        plotter=lambda: plot_equity_curves(result=result, output_path=output_path / "equity_curve.png"),
        output_path=output_path / "equity_curve.png",
        title="Equity Curves",
    )
    drawdowns_png = _safe_plot(
        plotter=lambda: plot_drawdowns(result=result, output_path=output_path / "drawdown_curve.png"),
        output_path=output_path / "drawdown_curve.png",
        title="Drawdowns",
    )
    weights_png = _safe_plot(
        plotter=lambda: plot_weights(weights=result["weights"], output_path=output_path / "weights_over_time.png"),
        output_path=output_path / "weights_over_time.png",
        title="Weights Over Time",
    )
    latest_decision_report_txt = write_latest_decision_report(
        result=result,
        output_path=output_path / latest_decision_filename,
    )

    return ReportArtifacts(
        daily_results_csv=table_paths["daily_results"],
        weights_csv=table_paths["weights"],
        target_weights_csv=table_paths["target_weights"],
        performance_summary_csv=table_paths["performance_summary"],
        decision_summary_csv=table_paths["decision_summary"],
        equity_curve_png=equity_curves_png,
        drawdown_curve_png=drawdowns_png,
        weights_over_time_png=weights_png,
        latest_decision_report_txt=latest_decision_report_txt,
    )
