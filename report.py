def plot_weights(weights: pd.DataFrame, output_path: str | Path) -> Path:
    """Plot historical portfolio weights as a stacked area chart."""

    if not MATPLOTLIB_AVAILABLE or plt is None:
        raise RuntimeError("matplotlib is not available in the current Python environment.")

    if weights.empty:
        raise ValueError("Weights DataFrame is empty.")

    history = weights.copy()
    if "date" in history.columns:
        history = history.set_index("date")
    history.index = pd.to_datetime(history.index)
    history = history.sort_index()

    figure, axis = plt.subplots(figsize=(12, 6))
    history.plot.area(ax=axis, linewidth=0.0, alpha=0.9)
    axis.set_title("Portfolio Weights Over Time")
    axis.set_xlabel("Date")
    axis.set_ylabel("Weight")
    axis.legend(loc="center left", bbox_to_anchor=(1.0, 0.5), ncol=1)
    figure.tight_layout()

    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    figure.savefig(path, dpi=150, bbox_inches="tight")
    plt.close(figure)
    return path


def _write_plot_placeholder(output_path: str | Path, title: str, error: Exception) -> Path:
    """Write a simple placeholder image when the main chart generation fails."""

    if not MATPLOTLIB_AVAILABLE or plt is None:
        path = Path(output_path)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.touch(exist_ok=True)
        return path

    figure, axis = plt.subplots(figsize=(10, 3))
    axis.axis("off")
    axis.text(0.01, 0.75, title, fontsize=14, fontweight="bold", ha="left", va="top")
    axis.text(
        0.01,
        0.45,
        "Chart generation failed for this run.",
        fontsize=11,
        ha="left",
        va="top",
    )
    axis.text(0.01, 0.18, str(error), fontsize=9, ha="left", va="top", wrap=True)
    figure.tight_layout()

    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    figure.savefig(path, dpi=150, bbox_inches="tight")
    plt.close(figure)
    return path


def _safe_plot(plotter: Callable[[], Path], output_path: str | Path, title: str) -> Path:
    """Run a plotting function safely and fall back to a placeholder image on failure."""

    try:
        return plotter()
    except Exception as exc:
        LOGGER.warning("Could not generate %s chart: %s", title, exc)
        try:
            return _write_plot_placeholder(output_path=output_path, title=title, error=exc)
        except Exception as placeholder_exc:
            LOGGER.warning(
                "Could not write placeholder chart %s either: %s",
                title,
                placeholder_exc,
            )
            path = Path(output_path)
            path.parent.mkdir(parents=True, exist_ok=True)
            path.touch(exist_ok=True)
            return path


def create_decision_summary(daily: pd.DataFrame) -> pd.DataFrame:
    """Create a summary table of decisions taken during the backtest."""

    if daily.empty:
        return pd.DataFrame(
            columns=[
                "decision",
                "count",
                "avg_net_benefit",
                "avg_realized_turnover",
                "emergency_count",
                "risk_gate_failed_count",
                "weekly_rebalance_count",
            ]
        )

    summary = (
        daily.groupby("decision", dropna=False)
        .agg(
            count=("decision", "size"),
            avg_net_benefit=("net_benefit", "mean"),
            avg_realized_turnover=("realized_turnover", "mean"),
            emergency_count=("emergency_condition", "sum"),
            risk_gate_failed_count=("risk_gate_failed", "sum"),
            weekly_rebalance_count=("weekly_rebalance_day", "sum"),
        )
        .reset_index()
        .sort_values(["count", "decision"], ascending=[False, True])
        .reset_index(drop=True)
    )

    totals = pd.DataFrame(
        [
            {
                "decision": "ALL",
                "count": int(len(daily)),
                "avg_net_benefit": float(pd.to_numeric(daily["net_benefit"], errors="coerce").mean()),
                "avg_realized_turnover": float(pd.to_numeric(daily["realized_turnover"], errors="coerce").mean()),
                "emergency_count": int(pd.to_numeric(daily["emergency_condition"], errors="coerce").fillna(0).sum()),
                "risk_gate_failed_count": int(pd.to_numeric(daily["risk_gate_failed"], errors="coerce").fillna(0).sum()),
                "weekly_rebalance_count": int(pd.to_numeric(daily["weekly_rebalance_day"], errors="coerce").fillna(0).sum()),
            }
        ]
    )
    return pd.concat([summary, totals], ignore_index=True)


def save_tables(result: dict[str, pd.DataFrame], output_dir: str | Path) -> dict[str, Path]:
    """Save the core output tables to disk."""

    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    daily_path = output_path / "daily_results.csv"
    weights_path = output_path / "weights.csv"
    target_weights_path = output_path / "target_weights.csv"
    performance_summary_path = output_path / "performance_summary.csv"
    decision_summary_path = output_path / "decision_summary.csv"

    daily = result.get("daily", pd.DataFrame()).copy()
    weights = result.get("weights", pd.DataFrame()).copy()
    target_weights = result.get("target_weights", pd.DataFrame()).copy()
    benchmark_returns = result.get("benchmark_returns", pd.DataFrame()).copy()

    try:
        if (
            daily.empty
            or "date" not in daily.columns
            or "portfolio_return_net" not in daily.columns
        ):
            raise ValueError("daily results are missing required columns for performance summary")

        strategy_returns_dict: dict[str, pd.Series] = {
            "strategy": daily.set_index("date")["portfolio_return_net"]
        }
        for column in benchmark_returns.columns:
            strategy_returns_dict[column] = benchmark_returns[column]

        performance_df = performance_summary(
            strategy_returns_dict,
            daily_records_dict={"strategy": daily},
        )
    except Exception as exc:
        LOGGER.warning("Could not build performance summary CSV normally: %s", exc)
        performance_df = pd.DataFrame(
            [
                {
                    "name": "strategy",
                    "observations": len(daily),
                    "error": str(exc),
                }
            ]
        )

    try:
        decision_df = create_decision_summary(daily)
    except Exception as exc:
        LOGGER.warning("Could not build decision summary CSV normally: %s", exc)
        decision_df = pd.DataFrame(
            [{"decision": "UNKNOWN", "count": len(daily), "error": str(exc)}]
        )

    _atomic_write_csv(daily, daily_path, index=False)
    _atomic_write_csv(weights, weights_path, index_label="date")
    _atomic_write_csv(target_weights, target_weights_path, index_label="date")
    _atomic_write_csv(performance_df, performance_summary_path, index=False)
    _atomic_write_csv(decision_df, decision_summary_path, index=False)

    return {
        "daily_results": daily_path,
        "weights": weights_path,
        "target_weights": target_weights_path,
        "performance_summary": performance_summary_path,
        "decision_summary": decision_summary_path,
    }


